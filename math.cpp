#include <iostream>
using namespace std;

int main() {
	cout << "This program will be demonstrating the different math operations found in c++\n";
	cout << "4 + 3 = " << 3 + 4 << "\n";
	cout << "4 - 3 = " << 3 - 4 << "\n";
	cout << "4 * 3 = " << 3 * 4 << "\n";
	cout << "(integer) 4 / 3 = " << 3/4 << "\n";
	cout << "(double) 4.0 / 3.0 = " << 3.0/4.0 << "\n";
	cout << "4 % 3 = " << 4 % 3 << "\n";
	cout << "3 % 4 = " << 3 % 4 << "\n";

}
